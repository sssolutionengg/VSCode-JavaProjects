package com.java.introduction.javadatatypes;

public class CharDataType {
    public static void main(String[] args) {

        char val = '\u0051';
        System.out.println(val);

        char letter1 = '9';
        System.out.println(letter1); // prints 9

        char letter2 = 65;
        System.out.println(letter2); // prints A
    }
}
